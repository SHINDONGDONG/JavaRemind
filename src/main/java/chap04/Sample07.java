package chap04;

public class Sample07 {

	public static void main(String[] args) {

		//and && 
		//or ||
		// xor ^
		// not !
		
		System.out.println( 1==1 & 2==3);
		System.out.println( 1 > 2 & 4 < 3);
		System.out.println( 1 < 2  & 4>2);

		System.out.println( 1==1 | 2==3);
		System.out.println( 1 > 2 |4 < 3);
		System.out.println( 1 < 2 |4>2);

		System.out.println( 1==1 ^2==3);
		System.out.println( 1 > 2^4 < 3);
		System.out.println( 1 < 2^4>2);
		
	}

}
